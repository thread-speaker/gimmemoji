// Gimmemoji was created using the chrome extension getting started guide
// as a reference. https://developer.chrome.com/extensions/getstarted

function pickEmoji(emojis) {
  emojis = emojis || EMOJILIST;
  var randomIndex = Math.floor(Math.random() * EMOJILIST.length);
  var randomEmoji = EMOJILIST[randomIndex];

  return randomEmoji;
}

function getCustomEmojis(callback) {
  // See https://developer.chrome.com/apps/storage#type-StorageArea
  // Check for chrome.runtime.lastError if correct
  chrome.storage.sync.get(CUSTOM, (items) => {
    callback(chrome.runtime.lastError ? [] : items);
  });
}

function saveCustomEmojis(emojiBlob) {
  StorageArea.clear(function () {
    var items = emojiBlob.split(/[ ,\r\n]+/);;
    chrome.storage.sync.set(CUSTOM, items);
  });
};

// Copying requires a visible, mounted text input.
function copyToClipboard(text) {
  var input = document.createElement('input');
  input.style.opacity = 0;
  document.body.appendChild(input);

  input.value = text;
  input.focus();
  input.select();

  // Copying can fail on occasion.
  try {
    document.execCommand('copy');
  } catch (error) {}

  input.remove();
}

document.addEventListener('DOMContentLoaded', () => {
  getCustomEmojis(function (customEmoji) {
    var completeEmojiList = EMOJILIST.concat(customEmoji);
    var randomEmoji = pickEmoji(completeEmojiList);
    var emojiDiv = document.getElementById('emoji');
    emojiDiv.innerHTML = randomEmoji;
    copyToClipboard(randomEmoji);
  });
});

